CREATE OR REPLACE TRIGGER display_livre_before_2005 
FOR INSERT OR UPDATE ON LIVRE  
FOR EACH ROW  
WHEN (LIVRE.ANNEE_EDITION <= 2005)  
DECLARE  
    CURSOR LISTLIVRE IS SELECT * FROM LIVRE WHERE LIVRE.ANNEE_EDITION <=2005,
	List LISTLIVRE%ROWTYPE;
BEGIN  
    OPEN LISTLIVRE;
    LOOP
	    FETCH LISTLIVRE INTO List;
	    EXIST WHEN LISTLIVRE%notfound;
	    DBMS_OUTPUT.put_line(List);
	ENDLOOP
    CLOSE LISTLIVRE
END;  